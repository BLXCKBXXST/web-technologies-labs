#!/usr/bin/env bash
# =============================================================
#  Практическая работа №7 — Подсказки для Desktop
#  Проверка доступа к почтовому серверу, nslookup
#
#  Запускать: sudo bash desktop_lab7_hints.sh
#  ВМ: Desktop (Ubuntu Desktop)
# =============================================================
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
# shellcheck source=config.sh
source "${SCRIPT_DIR}/config.sh"

echo "================================================================"
echo " Лабораторная №7 — Проверка почты с Desktop"
echo " mail FQDN : ${MAIL_FQDN}"
echo " mail IP   : ${MAIL_IP}"
echo " Домен     : ${DOMAIN}"
echo "================================================================"

if [[ $EUID -ne 0 ]]; then
  echo "[ОШИБКА] Запустите от root: sudo bash $0" >&2
  exit 1
fi

# ------------------------------------------------------------------
# ШАГ 1. Проверка DNS
# ------------------------------------------------------------------
echo
echo "--- Шаг 1: проверка DNS-имени mail ---"
nslookup "${MAIL_HOSTNAME}" && echo "[OK] DNS: ${MAIL_HOSTNAME} разрешается." || \
  echo "[ОШИБКА] nslookup ${MAIL_HOSTNAME} провалился — запусти gateway_add_mail_dns.sh на gateway."

# ------------------------------------------------------------------
# ШАГ 2. Проверка пинга
# ------------------------------------------------------------------
echo
echo "--- Шаг 2: ping ${MAIL_IP} ---"
ping -c3 "${MAIL_IP}" && echo "[OK] Связь с mail-сервером есть." || \
  echo "[ОШИБКА] ping ${MAIL_IP} не прошёл — проверь настройки сети ВМ mail."

# ------------------------------------------------------------------
# ШАГ 3. Проверка порта 443 (HTTPS)
# ------------------------------------------------------------------
echo
echo "--- Шаг 3: проверка порта 443 ---"
if command -v nc >/dev/null 2>&1; then
  nc -zv "${MAIL_IP}" 443 2>&1 && echo "[OK] Порт 443 открыт." || \
    echo "[ОШИБКА] Порт 443 закрыт — iRedMail возможно ещё не запущен."
else
  echo "[ИНФО] nc не найден, пропускаю проверку порта."
fi

# ------------------------------------------------------------------
# ШАГ 4. Подсказки по работе с веб-интерфейсом
# ------------------------------------------------------------------
echo
echo "================================================================"
echo " [ИНТЕРАКТИВНЫЙ ШАГ] Работа с почтовым сервером:"
echo
echo " 1. Открой браузер Firefox на Desktop"
echo
echo " 2. Панель администратора (iRedAdmin):"
echo "      https://${MAIL_FQDN}/iredadmin"
echo "    Логин  : ${POSTMASTER}"
echo "    Пароль : <указанный при установке>"
echo
echo " 3. Создай нового пользователя:"
echo "    iRedAdmin → Users → Add user"
echo "    Email: user1@${DOMAIN}"
echo "    (запомни пароль)"
echo
echo " 4. Веб-почта (Roundcubemail):"
echo "      https://${MAIL_FQDN}/mail"
echo
echo " 5. ЗАДАНИЕ — отправь письмо:"
echo "    Войди как ${POSTMASTER}"
echo "    Compose → To: user1@${DOMAIN}"
echo "    Subject: Тест ЛР-7"
echo "    Отправь письмо"
echo
echo " 6. Проверь получение:"
echo "    Войди как user1@${DOMAIN} → Inbox"
echo
echo " СОВЕТ: браузер выдаст предупреждение о самоподписанном"
echo " сертификате — нажми 'Advanced' → 'Accept the Risk'"
echo "================================================================"
