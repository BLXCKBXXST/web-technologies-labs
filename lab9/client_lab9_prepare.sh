#!/usr/bin/env bash
# =============================================================
#  Практическая работа №9 — Подготовка клиентской ВМ
#  Запускать на: desktop1 И wordpress
#
#  Запускать: sudo bash client_lab9_prepare.sh
#  ВМ: desktop1 / wordpress (клиенты Ansible)
# =============================================================
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
# shellcheck source=config.sh
source "${SCRIPT_DIR}/config.sh"

echo "================================================================"
echo " Лабораторная №9 — подготовка клиентской ВМ"
echo " Текущий хост: $(hostname)"
echo "================================================================"

if [[ $EUID -ne 0 ]]; then
  echo "[ОШИБКА] Запустите от root: sudo bash $0" >&2
  exit 1
fi

# ------------------------------------------------------------------
# ШАГ 1. Обновление пакетов
# ------------------------------------------------------------------
echo
echo "--- Шаг 1: apt update ---"
apt-get update -y
echo "[OK] apt update выполнен."

# ------------------------------------------------------------------
# ШАГ 2. Установка openssh-server и python3
# ------------------------------------------------------------------
echo
echo "--- Шаг 2: установка openssh-server, python3 ---"
apt-get install -y openssh-server python3
echo "[OK] Пакеты установлены."

# ------------------------------------------------------------------
# ШАГ 3. Включение и запуск SSH
# ------------------------------------------------------------------
echo
echo "--- Шаг 3: включение и запуск SSH ---"
systemctl enable ssh
systemctl start ssh

sleep 1
if systemctl is-active ssh &>/dev/null; then
  echo "[OK] SSH запущен и включён в автозагрузку."
else
  echo "[ОШИБКА] SSH не запустился. Смотри: journalctl -u ssh -n 20" >&2
  exit 1
fi

# ------------------------------------------------------------------
# ШАГ 4. Статус SSH
# ------------------------------------------------------------------
echo
echo "--- Шаг 4: статус SSH ---"
systemctl --no-pager status ssh

echo
echo "================================================================"
echo " Клиент готов к подключению Ansible."
echo " IP этой машины:"
ip -4 addr show scope global | awk '/inet /{print "   " $2}'
echo
echo " Вернись на gateway и выполни:"
echo "   bash gateway_lab9_ssh_copy.sh"
echo "================================================================"
