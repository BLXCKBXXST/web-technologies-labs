#!/usr/bin/env bash
# =============================================================
#  Практическая работа №9 — Часть 2
#  Копирование SSH-ключа на клиентские ВМ
#
#  Запускать: bash gateway_lab9_ssh_copy.sh  (БЕЗ sudo!)
#  ВМ: gateway (Ubuntu Server)
# =============================================================
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
# shellcheck source=config.sh
source "${SCRIPT_DIR}/config.sh"

echo "================================================================"
echo " Лабораторная №9 — копирование SSH-ключа на клиентов"
echo "================================================================"

if [[ $EUID -eq 0 ]]; then
  echo "[ОШИБКА] Этот скрипт нужно запускать БЕЗ sudo:" >&2
  echo "  bash $0" >&2
  exit 1
fi

# ------------------------------------------------------------------
# ШАГ 1. Проверка наличия ключа
# ------------------------------------------------------------------
echo
echo "--- Шаг 1: проверка SSH-ключа ---"
if [[ ! -f "${SSH_KEY}" ]]; then
  echo "[ОШИБКА] Ключ ${SSH_KEY} не найден."
  echo "[ИНФО] Сначала запусти: sudo bash gateway_lab9_setup.sh" >&2
  exit 1
fi
echo "[OK] Ключ найден: ${SSH_KEY}"

# ------------------------------------------------------------------
# ШАГ 2. ssh-copy-id → CLIENT1
# ------------------------------------------------------------------
echo
echo "--- Шаг 2: копирование ключа на ${CLIENT1_HOSTNAME} (${CLIENT1_IP}) ---"
echo "[ИНФО] Введи пароль пользователя ${CLIENT1_USER}@${CLIENT1_IP} когда спросит."
ssh-copy-id -i "${SSH_KEY}.pub" "${CLIENT1_USER}@${CLIENT1_IP}"

if [[ $? -eq 0 ]]; then
  echo "[OK] Ключ скопирован на ${CLIENT1_HOSTNAME}."
else
  echo "[ОШИБКА] Не удалось скопировать ключ на ${CLIENT1_HOSTNAME}." >&2
  exit 1
fi

# ------------------------------------------------------------------
# ШАГ 3. ssh-copy-id → CLIENT2
# ------------------------------------------------------------------
echo
echo "--- Шаг 3: копирование ключа на ${CLIENT2_HOSTNAME} (${CLIENT2_IP}) ---"
echo "[ИНФО] Введи пароль пользователя ${CLIENT2_USER}@${CLIENT2_IP} когда спросит."
ssh-copy-id -i "${SSH_KEY}.pub" "${CLIENT2_USER}@${CLIENT2_IP}"

if [[ $? -eq 0 ]]; then
  echo "[OK] Ключ скопирован на ${CLIENT2_HOSTNAME}."
else
  echo "[ОШИБКА] Не удалось скопировать ключ на ${CLIENT2_HOSTNAME}." >&2
  exit 1
fi

# ------------------------------------------------------------------
# ШАГ 4. Проверка ansible ping
# ------------------------------------------------------------------
echo
echo "--- Шаг 4: проверка ansible ping ---"
ansible clients -m ping

echo
echo "================================================================"
echo " SSH-ключи скопированы. Ansible может подключаться к клиентам."
echo
echo " СЛЕДУЮЩИЙ ШАГ:"
echo "   sudo bash gateway_lab9_run.sh"
echo "================================================================"
