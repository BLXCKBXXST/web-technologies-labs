#!/usr/bin/env bash
# =============================================================
#  Практическая работа №9 — Часть 3
#  Запуск Ansible playbook и проверка результатов
#
#  Запускать: bash gateway_lab9_run.sh  (БЕЗ sudo!)
#  ВМ: gateway (Ubuntu Server)
# =============================================================
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
# shellcheck source=config.sh
source "${SCRIPT_DIR}/config.sh"

echo "================================================================"
echo " Лабораторная №9 — запуск Ansible playbook"
echo " Playbook : ${PLAYBOOK_FILE}"
echo " Результат: ${MONITORING_DIR}/"
echo "================================================================"

# Скрипт должен работать ОТ ОБЫЧНОГО ПОЛЬЗОВАТЕЛЯ
# Ansible берёт SSH-ключ из ~/.ssh/ текущего пользователя
if [[ $EUID -eq 0 ]]; then
  echo "[ОШИБКА] Не запускайте этот скрипт через sudo!"
  echo "[ОШИБКА] Ansible не найдёт SSH-ключ из /root/.ssh/ — все клиенты будут UNREACHABLE."
  echo ""
  echo "   Запускай так:"
  echo "   bash gateway_lab9_run.sh"
  exit 1
fi

# ------------------------------------------------------------------
# ШАГ 1. Проверка что ansible установлен
# ------------------------------------------------------------------
echo
echo "--- Шаг 1: проверка Ansible ---"
if ! command -v ansible-playbook &>/dev/null; then
  echo "[ОШИБКА] ansible-playbook не найден. Сначала запусти gateway_lab9_setup.sh" >&2
  exit 1
fi
echo "[OK] $(ansible --version 2>&1 | head -1)"

# ------------------------------------------------------------------
# ШАГ 2. Проверка hosts-файла
# ------------------------------------------------------------------
echo
echo "--- Шаг 2: проверка инвентаря ---"
if [[ ! -f "${HOSTS_FILE}" ]]; then
  echo "[ОШИБКа] ${HOSTS_FILE} не найден. Сначала запусти gateway_lab9_setup.sh" >&2
  exit 1
fi
echo "[OK] Инвентарь: ${HOSTS_FILE}"
cat "${HOSTS_FILE}"

# ------------------------------------------------------------------
# ШАГ 3. Проверка ansible ping перед запуском
# ------------------------------------------------------------------
echo
echo "--- Шаг 3: ansible ping ---"
ansible clients -m ping || {
  echo "[ОШИБКА] Один или оба клиента недоступны через Ansible."
  echo "[ИНФО] Убедись что запустил gateway_lab9_ssh_copy.sh"
  exit 1
}
echo "[OK] Оба клиента отвечают на ping."

# ------------------------------------------------------------------
# ШАГ 4. Создание папки monitoring и передача прав текущему пользователю
# ------------------------------------------------------------------
echo
echo "--- Шаг 4: подготовка папки monitoring ---"
mkdir -p "${MONITORING_DIR}"
# Если папка принадлежит root (создана через sudo раньше) — передаём владельца
if [[ ! -w "${MONITORING_DIR}" ]]; then
  echo "[ИНФО] Папка ${MONITORING_DIR} недоступна для записи — меняю владельца..."
  sudo chown -R "${USER}:${USER}" "${MONITORING_DIR}"
  echo "[OK] Владелец изменён."
fi

# ------------------------------------------------------------------
# ШАГ 5. Запуск playbook
# ------------------------------------------------------------------
echo
echo "--- Шаг 5: запуск playbook ---"
ansible-playbook "${PLAYBOOK_FILE}"
echo "[OK] Playbook выполнен."

# ------------------------------------------------------------------
# ШАГ 6. Вывод собранных данных
# ------------------------------------------------------------------
echo
echo "--- Шаг 6: результаты мониторинга ---"
for FILE in "${MONITORING_DIR}"/*_info.txt; do
  if [[ -f "${FILE}" ]]; then
    echo
    echo "=== $(basename "${FILE}") ==="
    cat "${FILE}"
  fi
done

echo
echo "================================================================"
echo " Лабораторная №9 выполнена."
echo " Файлы мониторинга: ${MONITORING_DIR}/"
echo "================================================================"
