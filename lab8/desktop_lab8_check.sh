#!/usr/bin/env bash
# =============================================================
#  Практическая работа №8 — WordPress
#  Проверка доступа к WordPress с Desktop
#
#  Запускать: sudo bash desktop_lab8_check.sh
#  ВМ: desktop1
# =============================================================
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
source "${SCRIPT_DIR}/config.sh"

echo "================================================================"
echo " Лабораторная №8 — Проверка WordPress с Desktop"
echo " WP FQDN : ${WP_FQDN}"
echo " WP IP   : ${WP_IP}"
echo " Домен   : ${DOMAIN}"
echo "================================================================"

# ------------------------------------------------------------------
# ШАГ 1. Проверка DNS
# ------------------------------------------------------------------
echo
echo "--- Шаг 1: проверка DNS-имени wordpress ---"
nslookup "${WP_HOSTNAME}" && echo "[OK] DNS: ${WP_HOSTNAME} разрешается." || \
  echo "[ОШИБКА] nslookup ${WP_HOSTNAME} провалился — запусти gateway_lab8_dns.sh на gateway."

# ------------------------------------------------------------------
# ШАГ 2. Ping
# ------------------------------------------------------------------
echo
echo "--- Шаг 2: ping ${WP_IP} ---"
ping -c3 "${WP_IP}" && echo "[OK] Связь с WordPress-сервером есть." || \
  echo "[ОШИБКА] ping ${WP_IP} не прошёл — проверь сеть ВМ wordpress."

# ------------------------------------------------------------------
# ШАГ 3. Порт 80
# ------------------------------------------------------------------
echo
echo "--- Шаг 3: проверка порта 80 ---"
if command -v nc >/dev/null 2>&1; then
  nc -zv "${WP_IP}" 80 2>&1 && echo "[OK] Порт 80 открыт." || \
    echo "[ОШИБКА] Порт 80 закрыт — Apache возможно не запущен."
else
  echo "[ИНФО] nc не найден, пропускаю проверку порта."
fi

# ------------------------------------------------------------------
# ШАГ 4. Подсказки
# ------------------------------------------------------------------
echo
echo "================================================================"
echo " [ИНТЕРАКТИВНЫЙ ШАГ] Работа с WordPress:"
echo
echo " 1. Открой браузер Firefox на Desktop"
echo
echo " 2. Главная страница WordPress:"
echo "      http://${WP_IP}"
echo "      или http://${WP_FQDN}"
echo
echo " 3. Панель администратора:"
echo "      http://${WP_IP}/wp-admin"
echo "    Логин : admin (или тот, что указал при установке)"
echo "    Пароль: <указанный при установке>"
echo
echo " 4. ЗАДАНИЕ:"
echo "    а) Создай новую запись (Записи → Добавить новую)"
echo "    б) Укажи заголовок и текст, опубликуй"
echo "    в) Убедись, что запись отображается на главной странице"
echo "    г) Создай нового пользователя (Пользователи → Добавить нового)"
echo "       Email: user1@${DOMAIN}"
echo
echo "================================================================"
