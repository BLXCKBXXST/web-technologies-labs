#!/usr/bin/env bash
# =============================================================
#  Практическая работа №7 — Электронная почта
#  Пост-установка iRedMail: проверка сервисов и подсказки
#
#  Запускать ПОСЛЕ reboot: sudo bash mail_lab7_post.sh
#  ВМ: mail (192.168.N.5)
# =============================================================
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
# shellcheck source=config.sh
source "${SCRIPT_DIR}/config.sh"

echo "================================================================"
echo " Лабораторная №7 — Проверка mail-сервера после установки"
echo " FQDN     : ${MAIL_FQDN}"
echo " Домен    : ${MAIL_DOMAIN}"
echo "================================================================"

if [[ $EUID -ne 0 ]]; then
  echo "[ОШИБКА] Запустите от root: sudo bash $0" >&2
  exit 1
fi

# ------------------------------------------------------------------
# ШАГ 1. Проверка критических сервисов iRedMail
# ------------------------------------------------------------------
echo
echo "--- Шаг 1: статус сервисов iRedMail ---"

SERVICES=(postfix dovecot nginx slapd)
ALL_OK=1

for SVC in "${SERVICES[@]}"; do
  if systemctl is-active --quiet "${SVC}"; then
    echo "[OK] ${SVC} — запущен"
  else
    echo "[ОШИБКА] ${SVC} — НЕ запущен" >&2
    ALL_OK=0
  fi
done

if [[ ${ALL_OK} -eq 0 ]]; then
  echo "[ОШИБКА] Один или несколько сервисов не запущены. Проверь логи." >&2
  exit 1
fi

# ------------------------------------------------------------------
# ШАГ 2. Проверка портов
# ------------------------------------------------------------------
echo
echo "--- Шаг 2: проверка открытых портов ---"

for PORT in 25 143 465 587 993; do
  if ss -tlnp | grep -q ":${PORT}"; then
    echo "[OK] Порт ${PORT} слушается"
  else
    echo "[ИНФО] Порт ${PORT} не обнаружен (может быть нормой для данной конфигурации)"
  fi
done

# ------------------------------------------------------------------
# ШАГ 3. Тест DNS: MX-запись
# ------------------------------------------------------------------
echo
echo "--- Шаг 3: проверка MX-записи ---"

if host -t MX "${MAIL_DOMAIN}" "${GW_IP}" >/dev/null 2>&1; then
  MX_RESULT=$(host -t MX "${MAIL_DOMAIN}" "${GW_IP}")
  echo "[OK] MX: ${MX_RESULT}"
else
  echo "[ОШИБКА] MX-запись для ${MAIL_DOMAIN} не найдена" >&2
  echo "[ИНФО]   Проверь gateway_lab7_dns.sh на ВМ gateway" >&2
fi

# ------------------------------------------------------------------
# ШАГ 4. Подсказки: создание пользователей и отправка письма
# ------------------------------------------------------------------
echo
echo "================================================================"
echo " [ИНТЕРАКТИВНЫЙ ШАГ] Управление почтовым сервером:"
echo
echo " 1) На ВМ Desktop откройте браузер"
echo
echo "    Панель iRedAdmin:"
echo "      https://${MAIL_FQDN}/iredadmin"
echo "      или  https://${MAIL_IP}/iredadmin"
echo "    Логин  : ${POSTMASTER}"
echo "    Пароль : <тот, что указан при установке>"
echo
echo " 2) Создайте нового пользователя:"
echo "    iRedAdmin → Users → Add User"
echo "    Email: user1@${MAIL_DOMAIN}"
echo
echo " 3) Веб-почта Roundcube:"
echo "      https://${MAIL_FQDN}/mail"
echo "      или  https://${MAIL_IP}/mail"
echo
echo " 4) Задание: войдите под user1@${MAIL_DOMAIN},"
echo "    отправьте письмо себе или второму пользователю."
echo "    Убедитесь, что письмо доставлено во входящие."
echo "================================================================"
